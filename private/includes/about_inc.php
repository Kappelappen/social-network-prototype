<h1>About This Project</h1>

<p>
This project is a personal demonstration of a simple social media platform built from scratch using PHP and MySQL. 
The vision behind it is to create a digital meeting place where people 
can connect, explore profiles, interact through comments, and experience the fundamentals of a community-driven website.
</p>

<p>
The purpose of this project has not been to build a production-ready social network, but to deepen my understanding of 
full-stack web development by implementing core functionality myself.
</p>

<h2>Features</h2>

<p>
The current version includes user registration and authentication, secure login 
and session management, user account management, personal profile pages, profile editing, 
profile images, a member directory, a comment system for user profiles, pagination for comments 
and member listings, relational database design using MySQL, an object-oriented PHP architecture, 
and prepared SQL statements for secure database communication.
</p>

<h2>Technologies</h2>

<p>
This project was developed using PHP 8, MySQL, HTML5, CSS3, PDO (PHP Data Objects), and Object-Oriented Programming (OOP). 
The application is structured into reusable classes responsible for authentication, 
profile management, comments, pagination, database installation, and account handling.
</p>

<p>
Throughout the project I have focused on writing readable, maintainable, and modular code while learning how different 
components interact in a larger application.
</p>

<h2>Learning Journey</h2>

<p>
This project represents a significant part of my learning journey as a developer. Every feature has been an 
opportunity to understand new concepts, solve problems, and strengthen my programming skills through practical experience.
</p>

<p>
Whenever I reached challenges that exceeded my current knowledge, 
